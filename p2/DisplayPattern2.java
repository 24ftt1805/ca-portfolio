public class DisplayPattern2 {
    public static void main(String[] args) {
        
        String quote = "";

        System.out.println("   .=\"\"=.\r" +quote);
        System.out.println("  /      \\\r" +quote);
        System.out.println(" | O    O | \r" +quote);
        System.out.println(" \\   /\\   /\r" +quote);
        System.out.println(" /'-=\\/=-'\\\r" +quote);
        System.out.println("/ /      \\ \\     CHIP CHIP!");      
        System.out.println("\\/\\      /\\/\r" +quote);     
        System.out.println("   '.  .'\r" +quote);
        System.out.println("   /|\\/|\\");                                   

    }
}
